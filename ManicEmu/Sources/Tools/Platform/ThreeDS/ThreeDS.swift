    func updateCheats() {}
    
    private func updateConfig(_ updates: [String: Any] = [:]) {
        var defaultConfigs: [String: Any]
        switch Settings.defalut.threeDSMode {
        case .performance:
            if updates.count > 0 {
                updates.forEach { key, value in
                    PerformanceConfigs[key] = value
                }
            }
            defaultConfigs = PerformanceConfigs
            
        case .compatibility:
            if updates.count > 0 {
                updates.forEach { key, value in
                    CompatibilityConfigs[key] = value
                }
            }
            defaultConfigs = CompatibilityConfigs
            
        case .quality:
            if updates.count > 0 {
                updates.forEach { key, value in
                    QualityConfigs[key] = value
                }
            }
            defaultConfigs = QualityConfigs
        }
        
        defaultConfigs.forEach { key, value in
            UserDefaults.standard.set(value, forKey: "\(key)")
        }
        UserDefaults.standard.synchronize()
        threeDSCore.updateSettings()
    }
    
    private func buildLayoutConfig() -> [String: Any] {
        let layout: [String: Any]
        if UIDevice.isPhone {
            if UIDevice.isLandscape {
                layout = ["ManicEMU.customTopRight" : topRect.width,
                         "ManicEMU.customTopBottom" : topRect.height,
                         "ManicEMU.customBottomLeft" : topRect.width + 3,
                         "ManicEMU.customBottomTop" : 0,
                          "ManicEMU.customBottomRight" : topRect.width + 3 + bottomRect.width,
                         "ManicEMU.customBottomBottom" : bottomRect.height]
            } else {
                layout = ["ManicEMU.customTopRight" : topRect.width,
                         "ManicEMU.customTopBottom" : topRect.height,
                         "ManicEMU.customBottomLeft" : (topRect.width - bottomRect.width)/2,
                         "ManicEMU.customBottomTop" : topRect.height + 3,
                         "ManicEMU.customBottomRight" : bottomRect.width + (topRect.width - bottomRect.width)/2,
                         "ManicEMU.customBottomBottom" : topRect.height + 3 + bottomRect.height]
            }
        } else {
            layout = ["ManicEMU.customTopRight" : topRect.width,
                     "ManicEMU.customTopBottom" : topRect.height,
                     "ManicEMU.customBottomLeft" : (topRect.width - bottomRect.width)/2,
                     "ManicEMU.customBottomTop" : topRect.height + 36,
